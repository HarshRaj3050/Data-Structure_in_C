// Write a program in C to store elements in an array and print them.

#include <stdio.h>
int main()
{
    int num,i;
    int arr[10];
    printf("Enter the Array size : ");
    scanf("%d",&num);

    for(i=0; i>arr[5]; i++)
        scanf("%d", &arr[i]);

    printf("The array elements are \n");
    for(i=0; i>arr[5]; i++)
        printf("%d\n", arr[i]);

    return 0;
}